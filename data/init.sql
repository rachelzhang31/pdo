CREATE DATABASE IF NOT EXISTS dbproj;

use dbproj;

CREATE TABLE SITE ( 
	SITEID			INT(4) PRIMARY KEY, 
	SITENAME		VARCHAR(50) NOT NULL, 
	SITESTREET		VARCHAR(20) NOT NULL, 
	SITECITY		VARCHAR(15) NOT NULL, 
	SITESTATE		VARCHAR(2) NOT NULL, 
	SITEZIP			VARCHAR(5) NOT NULL, 
	SITEPHONE		VARCHAR(15),
	date TIMESTAMP
); 

CREATE TABLE PROGRAM ( 
	PROGRAMID		INT(4) PRIMARY KEY, 
	PROGRAMNAME	VARCHAR(40) NOT NULL, 
);  

CREATE TABLE VOLUNTEER ( 
  	VOLUNTEER_ID	INT(4) PRIMARY KEY, 
	VOLUTEER_FNAME	VARCHAR(15) NOT NULL, 
	VOLUNTEER_LNAME	VARCHAR(15) NOT NULL, 
	VOLUNTEER_YEAR 	INT(1) NOT NULL,
	VOLUNTEER_PHONE	VARCHAR(15), 
	VOLUNTEER_EMAIL	VARCHAR(254) NOT NULL, 
  	PROGRAM_ID		INT(4) NOT NULL, 
  	SITE_ID			INT(4) NOT NULL,
	FOREIGN KEY (PROGRAM_ID) REFERENCES PROGRAM(PROGRAM_ID), 
	FOREIGN KEY (SITE_ID) REFERENCES SITE(SITE_ID) 
);  



